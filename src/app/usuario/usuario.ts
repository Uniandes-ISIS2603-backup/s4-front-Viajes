/**
 * Esta clase representa un usuario.
 * Contiene toda la información de un usuario.
 */
export class Usuario {
  /**
   * El id del usuario.
   */
  id: number;

  /**
   * El nombre del usuario.
   */
  nombre: string;


  /**
   * La contraseña del usuario.
   */
  password: string;

  /**
   * El documento del usuario.
   */
  documento: string;

  /**
   * La edad del usuario.
   */
  edad: number;

  /**
   * El username del usuario.
   */
  userName: string;


}
