/**
* This class represents a Guide of TripBuilder. 
* It contains all the information relevant to the guide.
*/
export class Guia {
    
     /**
    * The guide's name
    */
    nombre: string;
    
    /**
    * The guide's document
    */
    documento: number;

    /**
    * The guide´s age
    */
    edad: number;

    /**
    * The guide´s language
    */
    idioma:string;

    /**
    * Guide´s ranking
    */
    puntuacion: number;
    
    sueldo:number;

    
}